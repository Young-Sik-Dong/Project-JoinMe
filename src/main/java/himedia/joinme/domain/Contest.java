package himedia.joinme.domain;

public class Contest{
	private String postNo;
	private String postTitle;
	private String postWriter;
	private String postDate;
	private Long index;
	private String contestField;
	private String companyName;
	private String targetName;
	private String hostName;
	private String reward;
	private String startDate;
	private String endDate;
	private String contestLink;
	
	public String getPostNo() {
		return postNo;
	}
	public void setPostNo(String postNo) {
		this.postNo = postNo;
	}
	public String getPostTitle() {
		return postTitle;
	}
	public void setPostTitle(String postTitle) {
		this.postTitle = postTitle;
	}
	public String getPostWriter() {
		return postWriter;
	}
	public void setPostWriter(String postWriter) {
		this.postWriter = postWriter;
	}
	public String getPostDate() {
		return postDate;
	}
	public void setPostDate(String postDate) {
		this.postDate = postDate;
	}
	public Long getIndex() {
		return index;
	}
	public String getContestField() {
		return contestField;
	}
	public void setContestField(String contestField) {
		this.contestField = contestField;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getTargetName() {
		return targetName;
	}
	public void setTargetName(String targetName) {
		this.targetName = targetName;
	}
	public String getHostName() {
		return hostName;
	}
	public void setHostName(String hostName) {
		this.hostName = hostName;
	}
	public String getReward() {
		return reward;
	}
	public void setReward(String reward) {
		this.reward = reward;
	}
	public String getStartDate() {
		return startDate;
	}
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	public String getEndDate() {
		return endDate;
	}
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
	public String getContestLink() {
		return contestLink;
	}
	public void setContestLink(String contestLink) {
		this.contestLink = contestLink;
	}
}
